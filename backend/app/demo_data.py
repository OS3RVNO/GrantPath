def build_scaled_snapshot(scale: int) -> Snapshot:
    if scale <= 1:
        return build_demo_snapshot()

    base = build_demo_snapshot()
    entities = []
    relationships = []

    for index in range(scale):
        suffix = "" if index == 0 else f"_s{index + 1}"
        for entity in base.entities:
            entities.append(
                entity.model_copy(
                    update={
                        "id": f"{entity.id}{suffix}",
                        "name": entity.name if index == 0 else f"{entity.name} [{index + 1}]",
                    }
                )
            )

        for relationship in base.relationships:
            relationships.append(
                relationship.model_copy(
                    update={
                        "id": f"{relationship.id}{suffix}",
                        "source": f"{relationship.source}{suffix}",
                        "target": f"{relationship.target}{suffix}",
                    }
                )
            )

    return base.model_copy(
        update={
            "tenant": f"{base.tenant} x{scale}",
            "entities": entities,
            "relationships": relationships,
        }
    )
