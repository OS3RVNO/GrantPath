def _entity(
    entity_id: str,
    name: str,
    kind: str,
    source: str,
    environment: str,
    description: str,
    *,
    criticality: int = 1,
    risk_score: int = 0,
    tags: list[str] | None = None,
    owner: str | None = None,
) -> Entity:
    return Entity(
        id=entity_id,
        name=name,
        kind=kind,
        source=source,
        environment=environment,
        description=description,
        criticality=criticality,
        risk_score=risk_score,
        tags=tags or [],
        owner=owner,
    )


def _relationship(
    rel_id: str,
    kind: str,
    source: str,
    target: str,
    label: str,
    rationale: str,
    *,
    permissions: list[str] | None = None,
    inherits: bool = False,
    temporary: bool = False,
    expires_at: str | None = None,
    removable: bool = False,
    metadata: dict[str, str] | None = None,
) -> Relationship:
    return Relationship(
        id=rel_id,
        kind=kind,
        source=source,
        target=target,
        label=label,
        rationale=rationale,
        permissions=permissions or [],
        inherits=inherits,
        temporary=temporary,
        expires_at=expires_at,
        removable=removable,
        metadata=metadata or {},
    )


